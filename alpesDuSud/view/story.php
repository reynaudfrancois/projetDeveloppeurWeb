		<h4 class="storyParagraphTitle"><span class="storyDateStage">4<sup>ème</sup> journée – jeudi 4 juin :</span> Col de Turini – Berthemont (970 m de dénivelé – 24 km)</h4>

		<p>Après ma première nuit en haute altitude, je repars à 8 h sur les sentiers. L'étape commence par une interminable descente en forêt de 1100 m de dénivelé vers le bas de la vallée de la Vésubie, qui dure 3 heures au total. Puis j'entame une succession de courtes montées et descentes, qui me conduisent d'abord à La Bollène-Vésubie, village perché à flanc de montagne, puis au bourg de Belvédère qui surplombe la vallée et offre, comme son nom l'indique, un très beau panorama sur les alentours. Il est alors 17 h et j'avais prévu initialement d'arrêter mon étape à cet endroit. Il me reste juste à trouver un endroit où planter mon bivouac. Mais la suite du parcours étant en forêt, je continue à marcher longtemps sans trouver un coin qui me convienne. Je poursuis donc les petites montées et descentes, jusqu'à me retrouver beaucoup plus loin que prévu, au village de Berthemont, à 21 h. Le bulletin météo m'annonçant une nuit claire, je renonce à planter ma tente et déplie juste duvet et tapis de sol. je passe ainsi la nuit à la belle étoile.</p>

