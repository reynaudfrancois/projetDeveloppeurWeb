		<h4 class="storyParagraphTitle"><span class="storyDateStage">11<sup>ème</sup> journée – venrdredi 12 juin :</span> Bousiéyas – Larche (1000 m de dénivelé – 18 km)</h4>

		<p>La météo annoncée n'étant vraiment pas bonne pour cette étape, je pars très tôt, à 5 h 30. Un peu avant 7 h, j'atteins le Camp des Fourches, 400 m plus haut. Une petite descente, puis la montée reprend pour atteindre le Pas de la Cavale, point culminant de ma randonnée avec 2671 m d'altitude, et qui permet de passer de la vallée de la Tinée au bassin de l'Ubaye. Pendant l'ascension, la pluie se met à tomber par moments, accompagnée d'un vent de plus en plus fort à l'approche du sommet du col. Un peu avant 9 h, une fois au sommet, je bascule donc sans attendre dans la descente, qui débute par un immense névé jusqu'au Lac de Derrière la Croix, situé 300 m plus bas. Une fois arrivé au lac, le plus dur reste pourtant à venir, même s'il ne reste que de la descente. Car l'averse reprend pour de bon. Je pars pour 3 heures de galère dans cette ambiance glaciale jusqu'à l'arrivée à Larche. Les paysages, sans aucun doute magnifiques en d'autres circonstances, ne présentent du coup plus aucun intérêt. Le seul objectif est simple, rallier la fin de l'étape au plus vite. Vers midi, alors que la pluie s'est calmée depuis une demi-heure, je touche enfin au but. Je m'installe au camping municipal, où je reste 30 minutes au chaud sous la douche. Puis je pars faire la sieste pour récupérer de cette journée compliquée.</p>

