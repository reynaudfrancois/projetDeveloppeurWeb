		<h4 class="storyParagraphTitle"><span class="storyDateStage">8<sup>ème</sup> journée – mardi 9 juin :</span> Vignols – Roya (930 m de dénivelé – 14 km)</h4>

		<p>A 6 h, je suis à nouveau en route, pour tenter d'éviter les orages de l'après-midi. Pour le moment, le ciel est parfaitement dégagé et vers 7 h 30, j'atteins le Col des Moulines, à près de 2000 m d'altitude. Depuis la crête, le paysage est superbe, avec un panorama à perte de vue sur lesalentours, et  notamment sur le village de Beuil. je m'arrête un quart d'heure pour profiter du décor. au loin, je vois passer quelques chamois et marmottes. La montée se poursuit en direction du Mont Démant, pas très loin du sommet du mont Mounier, que j'ai pu voir tout au long de cette ascension. Après être passé à près de 2600 m d'altitude, commence la longue descente vers Roya. les nuages commencent alors à s'accumuler dans le ciel et des orages commencent à se faire entendre depuis les vallées voisines. J'accélère alors le pas, les orages paraissant se rapprocher de plus en plus. Mais finalement, ils ne viendront pas au-dessus de moi, du moins pas avant l'arrivée. Par contre, une fois arrivé, il se met à pleuvoir des cordes. Impossible de planter ma tente dans ces conditions. j'avise alors un abri assez grand pour y déplier mon tapis de sol et y passer la nuit.</p>

