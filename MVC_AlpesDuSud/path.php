		<p> L'itinéraire rouge est le seul à passer par chacun des pays alpins et à traverser les Alpes du début à la fin. Il totalise 161 étapes au total.</p>

		<p>Les autres itinéraires (bleu, vert, jaune, violet) proposent des variantes à l'itinéraire rouge. Ils ne traversent qu'une partie du massif alpin.</p>

