			<h3 class="subTitle">LE MERCANTOUR</h3>

			<p>Le Mercantour est un massif situé à cheval sur les dépatements des Alpes-Maritimes, des Alpes-de-Haute-Provence et le Piémont italien. Il est délimité au Sud par l'Arrière-Pays Niçois, à l'Ouest par la vallée du Var, au Nord par les vallées du col de Larche, et à l'Est par les Alpes Ligures. Les sommets les plus connus sont le Mont Argentera, son point culminant à 3297 m, la Cime de la Bonette (2860 m), qui surplombe la route du Col de la Bonette, et le Mont Mounier (2817 m), qui domine les vallées de la Tinée et du Var. </p>

			<a href="https://fr.wikipedia.org/wiki/Massif_du_Mercantour-Argentera" target="new"><small>Source : Wikipedia</small></a>

