		<h4 class="storyParagraphTitle"><span class="storyDateStage">12<sup>ème</sup> journée – samedi 13 juin :</span> Larche – Maljasset (1380 m de dénivelé – 24 km)</h4>

		<p>Pour une fois, je me mets en route assez tard. il est déjà 10 h lorsque je quitte le camping. Le soleil, lui, est pour l'instant revenu et redonne de l'éclat aux paysages de la vallée de l'Ubayette. Il faut d'abord grimper au-dessus de Larche. Je laisse alors derrière moi le Mercantour, place au massif du Chambeyron. Après un peu plus d'une heure de grimpette, je bifurque pour quelques kilomètres de plat en direction du hameau de Saint-Ours. Commence alors la seconde ascension du jour, le Col de Mirandol, perché à 2433 m. Les nuages comment à revenir et un peu de pluie se met à tomber pendant la descente vers le hameau de Fouillouse, durant une demi-heure. Mais l'averse se calme rapidement cette fois, même si le cie reste couvert. un peu après Fouillouse, je passe sur le magnifique Pont du Châtelet : large de 3 m, posé au sommet d'une gorge étroite de 27 m, il domine l'Ubaye de 108 m de hauteur. Impressionnant. La fin du parcours est plus banale. Je suis la route goudronnée qui remonte la Haute-Ubaye jusqu'au hameau de Maljasset. Comme la brume commence en plus à recouvrir les sommets du Chambeyron, les paysages n'offrent à nouveau plus d'intérêt. J'atteins l'arrivée un peu après 19 h, où je passe la nuit dans un refuge.</p>

