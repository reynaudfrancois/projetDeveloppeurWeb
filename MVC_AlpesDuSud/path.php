		<h2 class="generalTitle">L'ITINERAIRE</h2>

		<p>Pour traverser les Alpes du Sud, je suis parti de Monaco, pour terminer à Valloire, au Sud de la Savoie. Je suis passé par l'Arrière-Pays Niçois, le Mercantour, le Chambeyron, le Queyras, les Ecrins et les Cerces. Les principales vallées traversées ont été la Vésubie, la Tinée, l'Ubaye, le Guil, la Durance et la Guisane.</p>

		<p>Après avoir quitté Monaco, j'ai pris de la hauteur sur la corniche de la Riviera vers le bourg de la Turbie. De là, j'ai parcouru pendant 5 étapes l'Arrière-Pays Niçois en passant notamment par Sospel et le Col de Turini, pour rejoindre la vallée de la Vésubie, que j'ai suivie jusqu'à  Saint-Martin-Vésubie.</p>

		
		<p>J'ai alors entamé le Mercantour. J'ai rejoint le Col Saint-Martin, où je suis passé dans la vallée de la Tinée, que j'ai suivie durant 6 étapes. Après avoir gagné le bas de la vallée, j'ai gravi le Mont Mounier presque jusqu'à son sommet, depuis Saint-Sauveur-sur-Tinée, en passant par le Refuge de Longon et le Col des Moulines. Je suis ensuite redescendu sur Saint-Etienne-de-Tinée en passant par la station d'Auron. De là, j'ai remonté le fond de vallée jusqu'au Pas de la Cavale, où je suis passé dans la vallée de l'Ubaye. Un peu plus bas, j'ai rejoint le village de Larche, où ma traversée du Mercantour s'est achevée.</p>

		<p>Puis j'ai marché duant 1 journée dans le massif du Chambeyron, pour rejoindre le fond de la vallée de l'Ubaye au refuge de Maljasset. Pour cela, j'ai emprunté le Col de Mirandol, avant de redescendre sur Fouillouse et le Pont du Châtelet, pour terminer en suivant le fond de vallée.</p>

		<p>Ensuite, j'ai entamé le Queyras pendant 3 étapes. Je suis monté au Col Tronchet pour passer dans la vallée du Guil. J'ai rejoint Ceillac, puis le fond de la Combe du Queyras, pour monter jusqu'au Refuge de Furfande, qui surplombe la combe. De là, j'ai gagné Guillestre et la vallée de la Durance. J'ai terminé le Queyras en traversant la forteresse de Mont-Dauphin.</p>

		<p>Puis j'ai randonné dans les Ecrins pendant les 3 étapes suivantes. J'ai d'abord pris de la hauteur sur la rive droite de la Durance avant de gagner le village de Vallouise, autour duquel se trouvent quelques sommets parmi les plus réputés des Ecrins. Ensuite, je suis passé devant le Mont-Pelvoux, pour rejoindre le Col de l'Eychauda, me pemettant de passer dans la vallée de la Guisane et de plonger sur Monêtier-les-bains, qui est une des stations du domaine de Serre-Chevalier.</p>

		<p>Enfin, le dernier jour, j'ai remonté la Guisane jusqu'au hameau du Lauzet, pour passer dans le massif des Cerces. J'ai gravi le Col de la Ponsonnière, situé juste en dessous du Galibier, et qui a marqué le fin des Alpes du Sud. De là, je suis descendu sur Valloire en passant par la Vallée de l'Aiguille Noire.</p>

