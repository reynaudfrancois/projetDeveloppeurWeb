			<h3 class="subTitle">LE QUEYRAS</h3>

			<p>Le Queyras est un massif des Hautes-Alpes qui s'étend au Sud de Briançon et à l'Est de la Vallée de la Durance, en grande partie sur la Vallée du Guil. D'un point de vue strictement géologique, il est approximativement délimité par la vallée de la Durance à l'Ouest et au Nord, la frontière italienne à l'Est, et la vallée du Guil eu Sud. D'un point de vue géographique, on a souvent tendance à l'étendre au massif voisin d'Escreins, situé entre les vallées du Guil et de la Haute Ubaye, et la route du Col de Vars.</p>

			<p>Les sommets les plus connus sont les Pics de Font Sancte (3385 m), point culminant de la région, et le Pic de Rochebrune (3320 m), point culminant du massif au sens strict. Le Mont Viso, bien que ne faisant pas partie du Queyras, en est toutefois très proche et domine les alentours du haut de ses 3841 m.</p>

			<p>A noter que dans la région du Queyras se trouve le plus haut village de France, Saint Véran (2042 m). On y trouve aussi des cols réputés où passe régulièrement le Tour de France et le Giro : le Col d'Izoard (2361 m), le Col Agnel (2744 m), et le Col de Vars (2108 m).</p>

			<a href="https://fr.wikipedia.org/wiki/Queyras_(homonymie)" target="new"><small>Source : Wikipedia</small></a>

		</article>

