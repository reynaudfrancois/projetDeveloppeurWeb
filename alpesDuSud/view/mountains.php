			<h3 class="subTitle">LES ECRINS</h3>

			<p>Le massif des Ecrins est situé dans les départements des Hautes-Alpes et de l'Isère. Il est délimité au l'Est et au Sud par la Durance, à l'Ouest par la vallée du Drac et la route du Col d'Ornon, au Nord par la vallée de la Romanche. Ce massif abrite de nombreux glaciers.</p>

			<p>Beaucoup de sommets y sont réputés pour l'alpinisme : la Barre des Ecrins, point culminant à 4101 m, le Dôme de Neige des Ecrins (4009 m), la Meije (3983 m), l'Ailefroide (3954 m), le Pelvoux (3946 m), le Râteau (3809 m), la Montagne des Agneaux (3663 m), le Pic de Neige Cordier (3614 m), pour ne citer que ceux-là.</p>

			<p>De nombreux glaciers se trouvent sur le massif, dont : le Glacier Blanc, qui donne accès à la plupart des sommets les plus réputés des Ecrins, le Glacier Noir, long de plus de 5 km, qui grimpe vers l'Ailefroide à l'Ouest du Pelvoux, et le Glacier du Mont-de-Lans situé au-dessus de la station des Deux-Alpes, y permettant la pratique du ski d'été.</p>

			<a href="https://fr.wikipedia.org/wiki/Massif_des_%C3%89crins" target="new"><small>Source : Wikipedia</small></a>

