		<h4 class="storyParagraphTitle"><span class="storyDateStage">7<sup>ème</sup> journée – lundi 8 juin :</span> Saint-Sauveur-sur-Tinée – Vignols (1450 m de dénivelé – 15 km)</h4>

		<p>A 6 h, je suis déjà en route. La journée s'annoce difficile, avec 1400 m de dénivelé à grimper presque d'une traite jusqu'au Refuge de Longon. Je monte d'abord au village de Roure, perché 600 m plus haut, que j'atteins vers 8 h. J'y fais une petite pause, mais l'ascension n'est pas terminée. Encore 800 m de dénivelé avant le refuge. La pente est d'abord raide, puis s'adoucit en suivant une piste forestière, avant de redevenir raide sur la fin. Et là, une fois sur le Plateau de Longon, le décor change. La végétation est plus verdoyante, les sommets plus imposants avec à leur tête le Mont Mounier, qui domine les alentours du haut de ses 2817 m d'altitude. Le passage à la haute montagne est saisissant, lorsque j'arrive aux premiers alpages du mercantour. C'est là que se trouve le Refuge de Longon, que j'atteins vers midi. Un orage étant annoncé dans l'après-midi, je patiente là avant de reprendre ma route. Celui-ci éclate vers 16 h. A 18 h, le temps se calme et je reprends ma route. Je passe les Portes de Longon, qui me font passer dans le vallon de Vionène, en bas duquel se trouve le hameau de Vignols, où je plante mon bivouac peu de temps avant la nuit.</p>

