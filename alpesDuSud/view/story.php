		<h2 class="generalTitle">LE RECIT</h2>

		<p>En juin 2015, j'ai effectué la traversée des Alpes du Sud. Parti de Monaco, je suis arrivé à Valloire, au début de la Savoie, 3 semaines plus tard, après avoir notamment traversé l'Arrière-Pays Niçois, le Mercantour,le Queyras et les Ecrins. Je suis également passé par les vallées de la Vésubie, de la Tinée, de l'Ubaye, du Guil, de la Durance, et de la Guisane. J'ai suivi pendant la plus grande partie du parcours la Via Alpina, qui est un réseau de sentiers permettant la traversée du massif alpin.</p>

