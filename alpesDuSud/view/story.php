		<p>Après une première nuit en bivouac, je me réveille vers 7 h. Une heure plus tard, je me mets en marche. L'étape commence sous le soleil par une descente jusqu'au fond du vallon, vers le hameau de Pas d'Ongrand. De là commence l'unique montée du jour, le Col du Farguet, 680 m de dénivelé. Après un peu plus de 2 heures d'ascension, je parviens au sommet. Je prends ma pause déjeuner, mais c'est à ce moment-là que la brume arrive et noie rapidement le paysage. La visibilité se réduit à 200 m à peine. Au moins, il ne pleut pas. Après une petite sieste, je repars pour une longue descente vers le bourg de Sospel. Le brouillard se dissipe petit à petit. A 16 h, c'est l'arrivée de cette étape. Jetrouve un terrain vague pas loin du centre-ville, à côté de la voie ferrée. C'est là que je plante mon second bivouac.</p>

