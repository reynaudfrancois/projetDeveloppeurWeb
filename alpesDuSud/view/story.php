		<h4 class="storyParagraphTitle"><span class="storyDateStage">1<sup>ère</sup> journée – lundi 1<sup>er</sup> juin :</span> Monaco – Peille (1200 m de dénivelé – 17 km)</h4>

		<p>Premier jour de juin. Je prends le départ de 3 semaines de marche devant le port de la Principauté. La grimpette commence d'entrée. Je monte d'abord tout en longeant la gare SNCF, qui est étagée tout en hauteur. Puis je bifurque vers le Jardin Exotique, où commence la Via Alpina. De là, je grimpe vers La Turbie, une petite ville qui domine Monaco du haut de ses 500 m. L'ascension offre une vue magnifique sur la Méditerranée ainsi que sur la Principauté. Les sommets alentours, eux, sont malheureusement cachés dans la brume. A La Turbie, où je passe devant le Trophée des Alpes, un ancien monument datant de l'époque romaine. Il reste un peu de montée jusqu'au Col de Guerre, perché 600 m au-dessus de la mer. Un dernier regard sur la Méditerranée, et je bascule dans l'Arrière-Pays Niçois, où la végétation est sèche et aride, typique du climat méditerranéen. La brume matinale s'est entre-temps dissipée et laisse place à un grand soleil. Il est presque midi quand j'arrive à Saint-Martin-de-Peille après une courte ascension. Le temps de pique- niquer, et je descends vers Peillon, charmant village perché, à travers un vallon abrupt. Puis je remonte à travers un autre vallon encaissé jusqu'à la chapelle Saint-Pancrace, qui marque la dernière crête de la journée avant l'arrivée. La chaleur devient alors presque caniculaire at avancer devient de plus en plus diffficile. Il est vrai que je n'ai pas encore gagné la haute montagne, où les effets des grosses chaleurs sont atténués par l'altitude. Une fois à la chapelle, il ne reste heureusement plus que la courte descente vers Peille, le but de cette première étape. Car en plus, je commence à avoir de plus en plus mal à la plante des pieds. L'arrivée étant à 2 km, je termine tranquillement, sans forcer. Une fois à Peille, le temps d'un arrêt à la fontaine, je gagne le Col Saint-Bernard, situé 80 m plus haut, où j'établis mon premier bivouac.</p>

