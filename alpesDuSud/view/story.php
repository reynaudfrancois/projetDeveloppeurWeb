		<p>A 8 h 30, je quitte Ceillac sous le soleil pour la première montée du jour, le Col de Bramousse, 600 m de dénivelé. Ce premier col se divise en 3 parties : d'abord une infinité de lacets très raides, suivi d'un court replat de quelques centaines de mètres, avant le raidard final en longue ligne droite. Une fois en haut, je peux profiter de la vue sur les sommets environnants du Queyras. Suit une longue descente en forêt jusqu'au Pont de Bramousse, qui traverse le Guil. Il reste alors à gravir la grosse difficulté de l'étape, la difficile ascencion vers le Refuge de Furfande, 7,5 km pour 1100 m de dénivelée, soit près de 15% de pente moyenne. Je grimpe tranquillement à mon rythme pendant un peu plus de 3 heures. Lorsque j'atteins le refuge, je suis épaté par la beauté de l'endroit. L'alpage est une sorte de balcon géant, avec une vue panoramique sur une bonne partie des sommets du Queyras. Il est juste dommage que les nuages masquent en patie les montagnes. Le refuge, lui, vient d'être remis à neuf. Je m'y installe pour la nuit. Petit à petit, la fin du parcours se rapproche. Il me reste à descendre sur Guillestre le lendemain, où je dois m'arrêter une journée. Ensuite, je n'aurai plus qu'à traverser les Ecrins sur 3 étapes, avant de rallier Valloire, le but final, lors de la dernière journée.</p>

