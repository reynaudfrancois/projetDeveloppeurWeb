		<p>Après un jour et demi de repos, je laisse l'Arrière-Pays Niçois pour le Mercantour. A 7 h, je quitte Saint-Martin-Vésubie pour grimper vers la petite station de la Colmiane, qui marque le passage de la vallée de la Vésubie à celle dela Tinée, dans laquelle je vais passer 6 journées. Puis suit une longue descente vers le bas de vallée par les villages de La Roche et de La Bolline. Celle-ci se fait sur des chemins qui longent en raccouci la route du Col saint-Martin. Ensuite vient une courte montée vers le village de Rimplas, avant de descendre vers Saint-Sauveur, le but de cette étape, que j'atteins vers 15 h.</p>

