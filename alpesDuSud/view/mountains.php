			<h3 class="subTitle">L'ARRIERE-PAYS NICOIS</h3>

			<p>L'Arrière-Pays Niçois est un massif qui s'étend au Nord de Nice. Il est approximativement délimité par la Méditerranée au Sud, les vallées du Var et de la Vésubie à l'Ouest, le Mercantour au Nord, et les Alpes Ligures à l'Est. Entre Nice et Vintimille, il surplombe la Côte d'Azur par des pentes abruptes. Cet endroit marque le début de la Riviera. Les sommets les plus connus sont la Pointe des Trois Communes, son point culminant à 2080 m, l'Authion (1889 m), et le Mont Agel (1148 m), qui surplombe Monaco.</p>

