		<h4 class="storyParagraphTitle"><span class="storyDateStage">3<sup>ème</sup> journée – mercredi 3 juin :</span> Sospel – Col de Turini (1850 m de dénivelé – 19 km)</h4>

		<p>Il est 7 h 30 quand j'entame l'étape la plus difficile du parcours, avec 1850 m de dénivelé au programme. Celle-ci commence par une petite montée assez facile le long de la vallée de la Bevera, le torrent qui passe à Sospel. Ce n'est qu'un simple échauffement avant les ascensions à venir, qui vont être de plus en plus dures. Après une courte descente pour traverser la rivière, c'est repati pour une nouvelle côte, qui ne présente guère plus de dénivelé, mais est par contre bien plus raide. Il est déjà presque midi quand je parviens sur la crête. Après la pause pique-nique, je repars pour une courte descente dans le Vallon de Ghiou, avant d'effectuer l'avant-dernière montée de la journée. Une fois au sommet, il faut redescendre sur le village de Moulinet, qui marque le pied de la longue rampe finale vers le Col de Turini. Il est déjà 16 h, et 900 m de dénivelé restent à gravir. L'ascension finale se divise en 2 parties : d'abord une pentesèche qui permet d'atteindre la crête 800 m plus haut, puis il faut longer cette même crête jusqu'à l'arrivée. Je commence à me demander si je vais pouvoir y parvenir avant la nuit, car avec déjà presque 1000 m de dénivelé dans les jambes, la fin d'étape s'annonce difficile. Et dès les premiers mètres, la fatigue se fait en effet sentir. Je grimpe sans forcer, prêt à chercher une solution de repli pour bivouaquer si besoin. Au bout de 3 heures, j'atteins enfin la crête. Le panaroma sur les alentours est magnifique : l'Arrière-Pays Niçois s'étend à perte de vue. Mais je dois repartir sans tarder pour ne pas être surpris par la nuit avant d'avoir atteint le Col de Turini. L'arrivée est encore à 5 km, et si le plus dur est fait, il est tout de même 19 h. Je longe la crête tout en profitant du panorama. En vue de l'arrivée, j'aperçois les premiers chamois du parcours. Enfin, à 20 h 45, j'arrive au but de l'étape, 1 heure avant la tombée de la nuit. Je plante mon bivouac juste au-dessus de la petite station du col.</p>

